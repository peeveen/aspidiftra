namespace Aspidiftra
{
	/// <summary>
	///   Enumeration of possible watermark text justification values.
	/// </summary>
	public enum Justification
	{
		/// <summary>
		///   Text will be left-justified.
		/// </summary>
		Left,

		/// <summary>
		///   Text will be right-justified.
		/// </summary>
		Right,

		/// <summary>
		///   Text will be centered.
		/// </summary>
		Centre
	}
}